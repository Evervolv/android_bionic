#ifndef CRT_LEGACY_WORKAROUND
	.arch armv5te
	.fpu softvfp
	.eabi_attribute 20, 1
	.eabi_attribute 21, 1
	.eabi_attribute 23, 3
	.eabi_attribute 24, 1
	.eabi_attribute 25, 1
	.eabi_attribute 26, 2
	.eabi_attribute 30, 4
	.eabi_attribute 18, 4
	.hidden	atexit
	.code	16
	.thumb_func
ENTRY(atexit)
.LFB0:
	.save	{r4, lr}
	push	{r4, lr}
.LCFI0:
	ldr	r3, .L3
	mov	r1, #0
	@ sp needed for prologue
.LPIC0:
	add	r3, pc
	ldr	r2, [r3]
	bl	__cxa_atexit
	pop	{r4, pc}
.L4:
	.align	2
.L3:
	.word	__dso_handle-(.LPIC0+4)
.LFE0:
END(atexit)
#endif
